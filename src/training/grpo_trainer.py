@contextmanager
def unwrap_model_for_generation(model, accelerator, gather_deepspeed3_params: bool = True):
    """生成前临时关闭梯度检查点，生成后恢复模型状态。"""
    unwrapped_model = accelerator.unwrap_model(model)
    # Check if gradient checkpointing is enabled, and disable it temporarily
    is_gradient_checkpointing = getattr(unwrapped_model, 'is_gradient_checkpointing', False)
    if is_gradient_checkpointing:
        unwrapped_model.gradient_checkpointing_disable()
    ####################################
    if accelerator.state.deepspeed_plugin is not None and accelerator.state.deepspeed_plugin.zero_stage == 3:
        if not gather_deepspeed3_params:
            yield accelerator.unwrap_model(model)
        else:
            import deepspeed

            with deepspeed.zero.GatheredParameters(model.parameters()):
                remove_hooks(model)
                yield accelerator.unwrap_model(model)
                add_hooks(model)
    else:
        yield unwrapped_model
    # Re-enable gradient checkpointing if it was enabled before
    if is_gradient_checkpointing:
        unwrapped_model.gradient_checkpointing_enable()
    #################

